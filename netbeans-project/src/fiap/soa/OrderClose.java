    private void orderProductUpdateQuantityButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_orderProductUpdateQuantityButtonActionPerformed

        logMessage.setText("Produto nÃ£o esta na lsita");

        for (OrderProduct o : orderProductList) {
            if (o.getId() == Integer.valueOf(orderProductId.getText())) {

                try {
                    // Verifica estoque
                    JSONObject json = Order.getProduct(o.getId());

                    int quantity = json.optInt("quantity", 0);

                    // Verifica estoque
                    if (Integer.valueOf(orderProductNewQuantity.getText()) > quantity) {
                        logMessage.setText("Sem estoque");
                    } else {
                        o.setQuantity(Integer.valueOf(orderProductNewQuantity.getText()));

                        logMessage.setText("Quantidade do produto atualizado com sucesso");
                    }

                    break;
                } catch (Exception ex) {
                    Logger.getLogger(OrderClose.class.getName()).log(Level.SEVERE, null, ex);
                    logMessage.setText("[ER] " + ex.getMessage());
                }
            }
        }

        // Calcula total do pedido
        orderTotal.setText(calculateTotal());

        // Exibir a lista de produtos
        orderList.setText(getTextProductList());
    }//GEN-LAST:event_orderProductUpdateQuantityButtonActionPerformed

