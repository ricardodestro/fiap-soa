    private String calculateTotal() {
        // Calcular total
        double total = 0;
        for (OrderProduct o : orderProductList) {
            total += o.getFinalPrice();
        }

        return String.valueOf(total);
    }

    private String getTextProductList() {

        StringBuilder sb = new StringBuilder();

        sb.append("ID\tNOME\tPREÃ‡O\tQTD\n");
        for (OrderProduct o : orderProductList) {
            sb.append(o.getId());
            sb.append("\t");
            sb.append(o.getName());
            sb.append("\t");
            sb.append(o.getFinalPrice());
            sb.append("\t");
            sb.append(o.getQuantity());
            sb.append("\n");
        }

        return sb.toString();
    }

