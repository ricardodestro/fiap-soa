    private void orderCloseButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_orderCloseButtonActionPerformed

        try {
            // Cria pedido com id do cliente e total da compra
            String message = "<store><action>create</action><order><client><id>" + clientId + "</id></client><total>" + calculateTotal() + "</total></order></store>";
            BrokerService bs = new BrokerService("order", message);

            JSONArray jsonList = new JSONArray(bs.getMessage());

            Integer orderId = null;

            if (jsonList.length() != 0) {
                orderId = jsonList.getJSONObject(0).getInt("GENERATED_KEY");
            }

            // Relaciona produtos com o pedido
            StringBuilder sb = new StringBuilder();
            sb.append("<store><action>addProducts</action>");
            sb.append("<order>");
            sb.append("<id>");
            sb.append(orderId);
            sb.append("</id>");
            sb.append("<products>");
            for (OrderProduct o : orderProductList) {
                sb.append("<product>");
                sb.append("<id>");
                sb.append(o.getId());
                sb.append("</id>");
                sb.append("<quantity>");
                sb.append(o.getQuantity());
                sb.append("</quantity>");
                sb.append("</product>");
            }
            sb.append("</products>");
            sb.append("</order>");
            sb.append("</store>");

            bs = new BrokerService("order", sb.toString());
            bs.getMessage();

            orderCloseButton.setVisible(false);
            orderCancelButton.setVisible(false);
            PanelProductData.setVisible(false);
            backButton.setVisible(true);

            logMessage.setText("Pedido concluÃ­do com sucesso");
        } catch (Exception e) {
            e.printStackTrace();
            logMessage.setText("[ER] " + e.getMessage());
        }


    }//GEN-LAST:event_orderCloseButtonActionPerformed

