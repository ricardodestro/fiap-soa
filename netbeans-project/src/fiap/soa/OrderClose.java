    private void orderProductRemoveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_orderProductRemoveButtonActionPerformed
        logMessage.setText("Produto nÃ£o esta na lsita");

        for (OrderProduct o : orderProductList) {
            if (o.getId() == Integer.valueOf(orderProductId.getText())) {
                orderProductList.remove(o);
                logMessage.setText("Produto removido com sucesso");
                break;
            }
        }

        // Calcula total do pedido
        orderTotal.setText(calculateTotal());

        // Exibir a lista de produtos
        orderList.setText(getTextProductList());
    }//GEN-LAST:event_orderProductRemoveButtonActionPerformed

